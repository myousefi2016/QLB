void QLB::init_GL()
{
	// Setup VBO's
	vbo_vertex.init(GL_ARRAY_BUFFER, GL_STATIC_DRAW);  

	// Setup shaders
	ShaderLoader shader_vertex;
	shader_vertex.load_from_file("shaders/vertex_shader.glsl", GL_VERTEX_SHADER);

	ShaderLoader shader_fragment;
	shader_fragment.load_from_file("shaders/fragment_shader.glsl",GL_FRAGMENT_SHADER);

	shader_.add_shaders(shader_vertex, shader_fragment);

	// Bind the VBO
	vbo_vertex.bind();
	
	// Allocate memory on the GPU
	vbo_vertex.malloc(sizeof(vertices)+sizeof(normals)+sizeof(colors));
	
	// Add the data
	vbo_vertex.BufferSubData(0, sizeof(vertices), vertices);
	vbo_vertex.BufferSubData(sizeof(vertices), sizeof(normals), normals);
	vbo_vertex.BufferSubData(sizeof(vertices)+sizeof(normals), sizeof(colors), colors);
	glCheckLastError();
	
	// Unbind the VBO
	vbo_vertex.unbind();

	GL_is_initialzed_ = true;
}

void QLB::render()
{
	if(!GL_is_initialzed_)
		FATAL_ERROR("QLB::OpenGL context is not initialized");

	// use the shader
	shader_.use_shader();

	// Bind the VBO
	vbo_vertex.bind();

	// Enable arrays
	glEnableClientState(GL_NORMAL_ARRAY);
	glEnableClientState(GL_COLOR_ARRAY);
	glEnableClientState(GL_VERTEX_ARRAY);

	// before draw, specify vertex and index arrays with their offsets
	glNormalPointer(GL_FLOAT, 0, (void*)sizeof(vertices));
	glColorPointer(3, GL_FLOAT, 0, (void*)(sizeof(vertices)+sizeof(normals)));
	glVertexPointer(3, GL_FLOAT, 0, 0);

	// Drawing
	glDrawArrays(GL_TRIANGLES, 0, 36);

	// Disable vertex arrays
	glDisableClientState(GL_VERTEX_ARRAY);
	glDisableClientState(GL_COLOR_ARRAY);
	glDisableClientState(GL_NORMAL_ARRAY);
 
	// Unbind the VBO
	vbo_vertex.unbind();
}

